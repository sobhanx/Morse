def escape_po(s: str) -> str:
    return s.replace("\\", "\\\\").replace('"', '\\"')


def fill_po(path: Path):
    content = path.read_text(encoding="utf-8")
    pattern = re.compile(
        r'(msgid "((?:\\.|[^"])*)"\n)(msgstr "((?:\\.|[^"])*)")',
        re.MULTILINE,
    )

    def repl(match):
        msgid_raw = match.group(2)
        msgid = bytes(msgid_raw, "utf-8").decode("unicode_escape")
        if not msgid:
            return match.group(0)
        translation = TRANSLATIONS.get(msgid)
        if translation is None:
            return match.group(0)
        return f'{match.group(1)}msgstr "{escape_po(translation)}"'

    updated = pattern.sub(repl, content)
    path.write_text(updated, encoding="utf-8")
    filled = sum(1 for k in TRANSLATIONS if f'msgid "{escape_po(k)}"' in content or f'msgid "{k}"' in content)
